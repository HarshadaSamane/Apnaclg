// Create a traffic light system that shows what to do based on colors

let color = 'red';

console.log(`The color is : ${color}`);

if(color == 'red') {
    console.log("Stop");
}
if(color === 'yellow') {
    console.log("Slow down your vehicle");
}
if(color == 'green') {
    console.log("You can go");
} 
